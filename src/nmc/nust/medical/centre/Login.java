    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        Connection con = MySqlConnection.Connect();
        PreparedStatement ps;
        ResultSet Rs;
        String Username = username.getText();
        String Password = password.getText();
        String UserType = usertype.getSelectedItem().toString();
       
        try{
            ps = con.prepareStatement("Select * From users where Username = ? and Password = ? and Usertype = ?");
            ps.setString(1, Username);
            ps.setString(2, Password);
            ps.setString(3, UserType);
            Rs = ps.executeQuery();                    //return require columns
            if(Rs.next())
            { 
                new NMC1(Username,UserType,Password).setVisible(true);
                username.setText("");
                password.setText("");
                username.requestFocus();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Incorrect Require fields.","Warning",JOptionPane.WARNING_MESSAGE);
                username.setText("");
                password.setText("");
                username.requestFocus();
                
            }
        }
        catch(SQLException e){
            System.out.println("Error"+e.getMessage());
        }
        
    }//GEN-LAST:event_LoginActionPerformed

