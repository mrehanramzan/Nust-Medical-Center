    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        Connection con = MySqlConnection.Connect();
        
        String result = JOptionPane.showInputDialog(this,"Enter Allocated ID.");
        
        try {
            PreparedStatement pst = con.prepareStatement("Select * from doctor where DoctorID = ?");
            pst.setString(1,result);
            ResultSet r = pst.executeQuery();
            if(r.next()){
                
                    new CreateAccount("Doctor",result).setVisible(true);
            }
            else{
                PreparedStatement pstd;
                ResultSet rt;
                pstd = con.prepareStatement("Select * from staff where staff_id = ?");
                pstd.setString(1,result);
                rt = pstd.executeQuery();
                if(rt.next()){
                    new CreateAccount("Receptionist",result).setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(this, "No ID Found" , "Alert",JOptionPane.WARNING_MESSAGE);
                }   
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

